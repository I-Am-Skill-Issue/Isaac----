# Класс Button отвечающий за кнопки в игре
class Button:
    def __init__(self, txt, pos, filter_check=None):
        self.text = txt  # Текст кнопки
        self.pos = pos  # Позиция кнопки на экране
        self.font = font  # Шрифт для отображения текста на кнопке
        self.default_color = 'white'  # Цвет кнопки в обычном состоянии (без наведения)
        self.hover_color = 'green'  # Цвет кнопки при наведении мыши
        self.off_color = 'red'
        self.filter_check = filter_check  # Функция для проверки состояния фильтра
        self.current_color = self.default_color  # Текущий цвет кнопки, инициализируется цветом по умолчанию

        # Создаем прямоугольник, охватывающий текст
        text_surface = self.font.render(self.text, True, self.current_color)
        self.button = text_surface.get_rect(topleft=self.pos)

    def update_color(self):
        # Определяет, какой цвет должен быть: белый, красный или зелёный
        if self.filter_check and not self.filter_check():
            self.current_color = self.off_color
        else:
            self.current_color = self.default_color
    
    def draw(self):
        # Отрисовка кнопок на экране с учётом цвета
        text_surface = self.font.render(self.text, True, self.current_color)
        screen.blit(text_surface, self.button.topleft)

    def check_mouse_hover(self):
        # Проверка наведения мыши на кнопку с целью подсветки
        if self.button.collidepoint(mouse.get_pos()):
            self.current_color = self.hover_color
        else:
            self.current_color = self.default_color  

# Функция создания кнопок
def create_button(text, position, *actions):
    button = Button(text, position)
    button.check_mouse_hover()
    button.draw()
    if mouse_clicked and button.button.collidepoint(mouse_pos):
        for action in actions:
            if callable(action):  
                action()  
            else:
                exec(action, globals())  # Передаем глобальное пространство имен

